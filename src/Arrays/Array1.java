package Arrays;

public class Array1 
	{
	public static void main(String[] args) {
	        int[] array = {3, 6, 1, 2, 4};  
	        int  sum = 0;
	        for (int i = 0; i < array.length; i++) {
	            sum = sum +array[i];  
	        }
	        System.out.println("Sum of all elements in array:" +sum);
	        
	    }
	}

